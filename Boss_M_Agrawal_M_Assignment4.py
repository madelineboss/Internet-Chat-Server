    # receive username from client
    data1 = sock.recv(1000)
    if (len(data1) == 0) :
        sock.close()
        return
    
    data2 = data1.decode().split(' ')[0]
    userName = data2.replace("\t", " ").replace("\n", "").replace("\r", "")
       
    # send welcome message
    str = f"Welcome to the Internet Chat Room, {userName}!\n\n"
